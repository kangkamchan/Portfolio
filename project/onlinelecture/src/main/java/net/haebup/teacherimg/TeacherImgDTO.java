package net.haebup.teacherimg;

public class TeacherImgDTO {
	private int fileNo;
	private int memberId;
	private int filePath;
	private int fileName;
	private int orgFileName;
	private int fileExt;
	private int idxfileSize;
	public int getFileNo() {
		return fileNo;
	}
	public void setFileNo(int fileNo) {
		this.fileNo = fileNo;
	}
	public int getMemberId() {
		return memberId;
	}
	public void setMemberId(int memberId) {
		this.memberId = memberId;
	}
	public int getFilePath() {
		return filePath;
	}
	public void setFilePath(int filePath) {
		this.filePath = filePath;
	}
	public int getFileName() {
		return fileName;
	}
	public void setFileName(int fileName) {
		this.fileName = fileName;
	}
	public int getOrgFileName() {
		return orgFileName;
	}
	public void setOrgFileName(int orgFileName) {
		this.orgFileName = orgFileName;
	}
	public int getFileExt() {
		return fileExt;
	}
	public void setFileExt(int fileExt) {
		this.fileExt = fileExt;
	}
	public int getIdxfileSize() {
		return idxfileSize;
	}
	public void setIdxfileSize(int idxfileSize) {
		this.idxfileSize = idxfileSize;
	}
	
	
	
}
