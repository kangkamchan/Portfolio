package net.haebup.courseoutline;

public class CourseOutlineDTO {
	private int idx;
	private String courseCode;
	private int outlineNo;
	private String content;
	private String time;
	private String videoPath;
	private String videoName;
	private String orgVideoName;
	private String videoExt;
	private int fileSize;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getCourseCode() {
		return courseCode;
	}
	public void setCourseCode(String courseCode) {
		this.courseCode = courseCode;
	}
	public int getOutlineNo() {
		return outlineNo;
	}
	public void setOutlineNo(int outlineNo) {
		this.outlineNo = outlineNo;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getVideoPath() {
		return videoPath;
	}
	public void setVideoPath(String videoPath) {
		this.videoPath = videoPath;
	}
	public String getVideoName() {
		return videoName;
	}
	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}
	public String getOrgVideoName() {
		return orgVideoName;
	}
	public void setOrgVideoName(String orgVideoName) {
		this.orgVideoName = orgVideoName;
	}
	public String getVideoExt() {
		return videoExt;
	}
	public void setVideoExt(String videoExt) {
		this.videoExt = videoExt;
	}
	public int getFileSize() {
		return fileSize;
	}
	public void setFileSize(int fileSize) {
		this.fileSize = fileSize;
	}
	
	
	
	
}
